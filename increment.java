package increment1;

public class increment {
	public static void main(String[] args) {

		 int val3=10;
		 int val4=20;

		// incrementing both pre and post fix
		System.out.println("incrementing both pre and post fix");
		System.out.println("pre-incrementing val1 :  "+ ++val3);
		System.out.println("pre-incrementing val2 :  "+ ++val4);
		System.out.println("post-incrementing val1 :  "+ val3++);
		System.out.println("post-incrementing val2 :  "+ val4++);
	}
}
